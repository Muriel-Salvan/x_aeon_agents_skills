    # Send a prompt to a Cline agent
    #
    # Parameters::
    # * *prompt_str* (String): Prompt to be sent
    # * *model* (String): Cline model to be used
    # * *plan_mode* (Boolean): Are we executing in Plan mode? [default: false]
    # * *config* (Hash): Cline config [default: {}]
    # * *skills* (Array<String>): List of skills to activate for this prompt [default: []]
    # * *skillkit_agents* (Boolean): Is AGENTS.md generated by skillkit? If true then a temporary AGENTS.md file will be used to deactivate non-desired skills. [default: false]
    # * *cli_args* (String): Additional CLI arguments [default: '']
    # * *on_message* (Proc or nil): Callback to be called everytime a new message happens in the task being executed, or nil if no callback [default: nil]
    #   * Parameters::
    #     * *message* (Hash): Message that has happened
    #     * *last* (Boolean): Is this the last message fetched?
    #     * *previous_version* (Hash or nil): Previous version of this message if it got updated, or nil if it is a new one
    #     * *usage* (Hash<Integer,Hash<Symbol,Object>>): Information on the usage, per timestamp. The information can have the following properties:
    #       * *cost* (Float): Query cost, in $.
    #       * *input_tokens* (Integer): Input tokens of the query
    #       * *output_tokens* (Integer): Output tokens of the query
    #       * *cache_read_tokens* (Integer): Cache read tokens of the query
    #       * *cache_write_tokens* (Integer): Cache write tokens of the query
    #       * *context_tokens* (Integer): Size of the context
    #       * *context_tokens_limit* (Integer): Limit of the size of the context
    # * *stdout_echo* (Boolean): Do we echo stdout of Cline CLI? [default: false]
    # * *ignore_partials* (Boolean): Should we ignore partial messages? If true, then on_message will only be called for messages that have been fully received. [default: false]
    def prompt(prompt_str, model:, plan_mode: false, config: {}, skills: [], skillkit_agents: false, cli_args: '', on_message: nil, stdout_echo: false, ignore_partials: false)
      Helpers.with_temp_dir(sub_dir: 'tmp/cline') do |temp_dir|
        config_dir = "#{temp_dir}/cline_config"
        log_debug "Temporary Cline config dir: #{config_dir}"

        # Authenticate and generate the configuration directory
        system "cline auth --config #{config_dir} --provider cline --apikey #{@api_key} --modelid #{model}", exception: true
        log_debug "Cline CLI authenticated successfully in config dir #{config_dir} for model #{model}"

        # Merge configuration options from payload into the globalState.json file
        global_state_path = "#{config_dir}/data/globalState.json"
        File.write(
          global_state_path,
          JSON.pretty_generate(
            Helpers.deep_merge(
              JSON.parse(File.read(global_state_path)),
              config
            )
          )
        )

        with_selected_skills(skills, config_dir:, skillkit_agents:) do
          with_messages_monitoring(config_dir:, model:, on_message:, ignore_partials:) do
            @task_id = nil
            begin
              @next_prompt = prompt_str
              idx_prompt = 0
              while !@next_prompt.nil? && @next_prompt != :exit
                # Generate prompt file
                prompt_file = "#{temp_dir}/prompt_#{idx_prompt}.txt"
                # Make sure we always create Unix-style files.
                File.write(prompt_file, @next_prompt.is_a?(String) ? @next_prompt.gsub("\r", '') : JSON.pretty_generate(@next_prompt), mode: 'wb')
                @next_prompt = nil
                @remove_conversation_index = nil
                idx_prompt += 1
                run_status = {}
                begin
                  # Run the agent
                  cmd = "cline --config #{config_dir} #{plan_mode ? '--plan' : '--act'} #{Logger.debug ? '--verbose' : ''} --json #{cli_args} #{@task_id.nil? ? '' : "--taskId #{@task_id}"} < #{prompt_file}"
                  log_debug "Cline CLI: #{cmd}"
                  run_status = XAeonAgentsSkills::Helpers.run_cmd(
                    cmd,
                    expected_exit_status: nil,
                    on_start: proc do |_stdin, _stdout, _stderr, wait_thr|
                      @cline_pid = wait_thr.pid
                    end,
                    on_stdout: proc do |line|
                      puts line if stdout_echo
                      if line =~ /^\{"type":"task_started","taskId":"(\d+)"\}$/
                        @task_id = Integer(Regexp.last_match[1])
                        log_debug "Started task ID #{@task_id}"
                      end
                    end,
                    on_stderr: proc { |line| $stderr.puts line }
                  )
                ensure
                  @cline_pid = nil
                end
                # Wait for the messages thread to stop processing messages
                sleep 0.1 while @processing_messages
                # In case of errors, we may have to remove some conversations
                unless @remove_conversation_index.nil?
                  log_debug "Remove conversation #{@remove_conversation_index} from task's messages"
                  File.write(
                    current_task_messages_file(config_dir),
                    current_task_messages(config_dir).
                      select { |message| message[:conversationHistoryIndex] != @remove_conversation_index }.
                      to_json
                  )
                end
                # If we are having a next prompt, then we can ignore the exit status.
                # The process could have been interrupted on purpose, or because of an error that we want to retry.
                raise "Unexpected exit status: #{run_status[:exit_status]}" if @next_prompt.nil? && run_status[:exit_status] != 0
              end
            ensure
              @task_id = nil
            end
          end
        end
      end
    end

    # Give some user feedback on a running task.
    # This will interrupt the current task, and resume it with the user feedback.
    #
    # Parameters::
    # * *message* (String or Synbol): The feedback to append, or symbols for some commands:
    #   * *exit*: Interrupt the task
    def user_feedback(message)
      raise 'No task current running' if @cline_pid.nil?
      log_debug "=> Send user feedback #{message}"
      @next_prompt = message
      log_debug "Interrupt current task with PID #{@cline_pid}"
      if Gem.win_platform?
        system("taskkill /PID #{@cline_pid} /T /F 2>&1 || exit /B 0", exception: true)
      else
        Process.kill('INT', @cline_pid)
      end
    end

